  render(ctx: CanvasRenderingContext2D) {
    this.field.forEach((row, i) => {
      row.forEach((item, j) => {
        if (typeof item !== 'number') {
          item.render(j, i, ctx)
        }
      })
    })
  }

  append(figure: Figure) {
    figure.getTemplate().forEach((row, i) => {
      row.forEach((item, j) => {
        if (typeof item !== 'number') {
          this.field[figure.getY() + i][figure.getX() + j] = item
        }
      })
    })
  }
